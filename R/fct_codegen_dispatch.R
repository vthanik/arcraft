fct_codegen_ard_tte <- function(grouping, var_configs) {
  trt <- grouping$trt_var
  paramcd <- var_configs$paramcd %||% "PFS"
  paste(c(
    "# --- ARD: Time-to-Event ---",
    "",
    paste0('tte_data <- adtte |> filter(PARAMCD == "', paramcd, '")'),
    "",
    "# KM estimates",
    paste0("km_fit <- survfit(Surv(AVAL, 1 - CNSR) ~ ", trt, ", data = tte_data)"),
    "km_summary <- summary(km_fit)",
    "",
    "# Log-rank test",
    paste0("lr_test <- survdiff(Surv(AVAL, 1 - CNSR) ~ ", trt, ", data = tte_data)"),
    "lr_pval <- 1 - pchisq(lr_test$chisq, df = length(lr_test$n) - 1)",
    "",
    "# Cox PH for hazard ratio",
    paste0("cox_fit <- coxph(Surv(AVAL, 1 - CNSR) ~ ", trt, ", data = tte_data)"),
    "cox_tidy <- tidy(cox_fit, conf.int = TRUE, exponentiate = TRUE)",
    ""
  ), collapse = "\n")
}

