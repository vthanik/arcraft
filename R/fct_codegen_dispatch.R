fct_codegen_ard_ae_socpt <- function(template, grouping) {
  trt <- grouping$trt_var
  filter_line <- switch(template,
    ae_socpt = "",
    ae_related = '  filter(AEREL %in% c("RELATED", "PROBABLE", "POSSIBLE", "Y")) |>',
    ae_grade34 = '  filter(AETOXGR %in% c("3", "4", "5")) |>',
    ae_serious = '  filter(AESER == "Y") |>',
    ae_fatal = '  filter(AEOUT == "FATAL" | AESDTH == "Y") |>',
    ae_dose_reduced = '  filter(AEACN == "DOSE REDUCED") |>',
    ae_drug_interrupted = '  filter(AEACN == "DRUG INTERRUPTED") |>',
    ae_drug_withdrawn = '  filter(AEACN == "DRUG WITHDRAWN") |>',
    ae_frequency = "",
    ""
  )

  lines <- c(
    paste0("# --- ARD: AE SOC/PT (", template, ") ---"),
    "",
    paste0("big_n <- adsl |> count(", trt, ', name = "N")'),
    ""
  )

  if (nzchar(filter_line)) {
    lines <- c(lines, "adae_filtered <- adae |>", filter_line, "  identity()", "")
  } else {
    lines <- c(lines, "adae_filtered <- adae", "")
  }

  lines <- c(lines,
    "# Any TEAE row",
    paste0("ard_any <- adae_filtered |>"),
    paste0("  distinct(USUBJID, ", trt, ") |>"),
    paste0("  count(", trt, ", name = \"n\")"),
    "",
    "# SOC-level counts",
    paste0("ard_soc <- adae_filtered |>"),
    paste0("  distinct(USUBJID, ", trt, ", AEBODSYS) |>"),
    paste0("  count(", trt, ", AEBODSYS)"),
    "",
    "# PT-level counts",
    paste0("ard_pt <- adae_filtered |>"),
    paste0("  distinct(USUBJID, ", trt, ", AEBODSYS, AEDECOD) |>"),
    paste0("  count(", trt, ", AEBODSYS, AEDECOD)"),
    ""
  )

  if (template == "ae_frequency") {
    lines <- c(lines,
      "# Sort SOC and PT by descending frequency",
      "soc_order <- ard_soc |> group_by(AEBODSYS) |> summarise(total = sum(n)) |> arrange(desc(total))",
      "pt_order <- ard_pt |> group_by(AEBODSYS, AEDECOD) |> summarise(total = sum(n)) |> arrange(desc(total))",
      ""
    )
  }

  paste(lines, collapse = "\n")
}

