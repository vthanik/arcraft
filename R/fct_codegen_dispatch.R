# Template-aware code generation dispatch
# Shared header/data/format sections + template-specific ARD section

fct_codegen_dispatch <- function(template, data_cfg, grouping, var_configs, format_cfg,
                                  combined_groups = NULL) {
  output_type <- fct_template_output_type(template)

  sections <- c(
    fct_codegen_header_dispatch(template, output_type),
    fct_codegen_data_dispatch(template, data_cfg),
    fct_codegen_ard_dispatch(template, grouping, var_configs),
    if (output_type == "table") fct_codegen_format(format_cfg) else "",
    if (output_type == "table") fct_codegen_render(format_cfg) else "",
    if (output_type == "figure") fct_codegen_figure_render(template, format_cfg) else "",
    if (output_type == "listing") fct_codegen_listing_render(format_cfg) else ""
  )
  paste(sections[nzchar(sections)], collapse = "\n")
}

fct_codegen_header_dispatch <- function(template, output_type) {
  libs <- c("dplyr", "tidyr", "arframe")
  if (output_type == "figure") libs <- c(libs, "ggplot2")
  if (template == "km_plot") libs <- c(libs, "survival", "patchwork")
  if (template %in% c("tte", "forest")) libs <- c(libs, "survival", "broom")

  paste0(
    "# =============================================================================\n",
    "# Generated by arbuilder\n",
    "# Template: ", template, "\n",
    "# Date: ", Sys.Date(), "\n",
    "# =============================================================================\n",
    "\n",
    paste0("library(", libs, ")", collapse = "\n"),
    "\n\n"
  )
}

fct_codegen_data_dispatch <- function(template, data_cfg) {
  required <- fct_template_required_datasets(template)
  pop_flag <- data_cfg$pop_flag

  lines <- "# --- Data ---"
  for (ds in required) {
    path <- data_cfg$paths[[ds]] %||% paste0("data/", ds, ".rds")
    lines <- c(lines, paste0(ds, ' <- readRDS("', path, '")'))
  }

  # Apply pop filter to ADSL
  if (!is.null(pop_flag) && nzchar(pop_flag)) {
    lines <- c(lines, paste0('adsl <- adsl |> filter(', pop_flag, ' == "Y")'))
  }

  paste(c(lines, ""), collapse = "\n")
}

fct_codegen_ard_dispatch <- function(template, grouping, var_configs) {
  trt_var <- grouping$trt_var

  switch(template,
    # Demographics — use existing codegen
    demog = fct_codegen_ard(grouping, var_configs),

    # Disposition
    disposition = fct_codegen_ard_disposition(grouping, var_configs),

    # Populations
    populations = fct_codegen_ard_populations(grouping),

    # Hierarchical tables (MH, CM)
    medical_history =, prior_meds =, concomitant_meds =
      fct_codegen_ard_hierarchical(template, grouping, var_configs),

    # AE tables
    ae_summary = fct_codegen_ard_ae_summary(grouping),
    ae_socpt =, ae_related =, ae_grade34 =, ae_serious =,
    ae_fatal =, ae_dose_reduced =, ae_drug_interrupted =,
    ae_drug_withdrawn =, ae_frequency =
      fct_codegen_ard_ae_socpt(template, grouping),
    ae_by_severity = fct_codegen_ard_ae_severity(grouping),

    # Exposure
    exposure = fct_codegen_ard_exposure(grouping, var_configs),

    # TTE
    tte = fct_codegen_ard_tte(grouping, var_configs),

    # Response
    response = fct_codegen_ard_response(grouping, var_configs),

    # Continuous efficacy
    efficacy_cont = fct_codegen_ard_efficacy_cont(grouping, var_configs),

    # BDS (lab/vitals/ecg)
    lab_results =, vitals =, ecg = fct_codegen_ard_bds(grouping, var_configs),

    # Shift
    lab_shift =, vitals_shift =, ecg_shift = fct_codegen_ard_shift(grouping, var_configs),

    # Marked abnormalities
    marked_abnorm = fct_codegen_ard_marked(grouping, var_configs),

    # Figures
    km_plot = fct_codegen_fig_km(grouping, var_configs),
    waterfall = fct_codegen_fig_waterfall(grouping, var_configs),
    forest = fct_codegen_fig_forest(grouping, var_configs),
    swimmer = fct_codegen_fig_swimmer(grouping, var_configs),

    # Listings
    listing_demographics =, listing_ae =, listing_lab =,
    listing_discontinued =, listing_custom =
      fct_codegen_listing(template, var_configs),

    # Fallback
    paste("# --- ARD Construction ---\n# Template:", template, "(codegen not yet implemented)\n")
  )
}

# ── Template-specific codegen functions ──

fct_codegen_ard_disposition <- function(grouping, var_configs) {
  trt <- grouping$trt_var
  paste(c(
    "# --- ARD: Disposition ---",
    "",
    paste0("big_n <- adsl |> count(", trt, ', name = "N")'),
    "",
    "# Completion status",
    paste0("ard_completion <- adsl |>"),
    paste0("  count(", trt, ", EOSSTT) |>"),
    paste0("  group_by(", trt, ") |>"),
    "  mutate(pct = n / sum(n) * 100) |>",
    "  ungroup()",
    "",
    "# Discontinuation reasons",
    paste0("ard_disc_reason <- adsl |>"),
    paste0('  filter(EOSSTT == "DISCONTINUED") |>'),
    paste0("  count(", trt, ", DCREASCD) |>"),
    paste0("  group_by(", trt, ") |>"),
    "  mutate(pct = n / sum(n) * 100) |>",
    "  ungroup()",
    ""
  ), collapse = "\n")
}

fct_codegen_ard_populations <- function(grouping) {
  trt <- grouping$trt_var
  flags <- grouping$analysis_vars
  if (length(flags) == 0) flags <- c("SAFFL", "ITTFL", "EFFFL")

  lines <- c("# --- ARD: Analysis Populations ---", "")
  for (fl in flags) {
    lines <- c(lines,
      paste0("ard_", tolower(fl), " <- adsl |>"),
      paste0("  group_by(", trt, ") |>"),
      paste0('  summarise(n = sum(', fl, ' == "Y", na.rm = TRUE), .groups = "drop") |>'),
      paste0("  mutate(N = n(), pct = n / N * 100)"),
      ""
    )
  }
  paste(lines, collapse = "\n")
}

fct_codegen_ard_hierarchical <- function(template, grouping, var_configs) {
  trt <- grouping$trt_var
  ds <- switch(template, medical_history = "adcm", prior_meds = "adcm", concomitant_meds = "adcm", "adcm")
  parent <- var_configs$.parent_var %||% "CMCLAS"
  child <- var_configs$.child_var %||% "CMDECOD"

  paste(c(
    paste0("# --- ARD: Hierarchical (", template, ") ---"),
    "",
    paste0("# Merge treatment from ADSL"),
    paste0("analysis <- ", ds, " |>"),
    paste0('  inner_join(adsl |> select(USUBJID, ', trt, '), by = "USUBJID")'),
    "",
    paste0("big_n <- adsl |> count(", trt, ', name = "N")'),
    "",
    "# Parent-level counts (subjects with any event in category)",
    paste0("ard_parent <- analysis |>"),
    paste0("  distinct(USUBJID, ", trt, ", ", parent, ") |>"),
    paste0("  count(", trt, ", ", parent, ") |>"),
    paste0("  group_by(", trt, ") |>"),
    "  ungroup()",
    "",
    "# Child-level counts",
    paste0("ard_child <- analysis |>"),
    paste0("  distinct(USUBJID, ", trt, ", ", parent, ", ", child, ") |>"),
    paste0("  count(", trt, ", ", parent, ", ", child, ") |>"),
    paste0("  group_by(", trt, ") |>"),
    "  ungroup()",
    ""
  ), collapse = "\n")
}

