fct_codegen_fig_forest <- function(grouping, var_configs) {
  trt <- grouping$trt_var
  paste(c(
    "# --- Figure: Forest Plot ---",
    "",
    "# Subgroup analyses — HR + 95% CI",
    "subgroups <- list(",
    '  list(var = "SEX", label = "Sex"),',
    '  list(var = "AGEGR1", label = "Age Group"),',
    '  list(var = "RACE", label = "Race")',
    ")",
    "",
    "forest_data <- lapply(subgroups, function(sg) {",
    paste0("  fit <- coxph(Surv(AVAL, 1 - CNSR) ~ ", trt, ", data = adtte,"),
    "    subset = !is.na(adtte[[sg$var]]))",
    "  tidy(fit, conf.int = TRUE, exponentiate = TRUE) |>",
    "    mutate(subgroup = sg$label)",
    "}) |> bind_rows()",
    "",
    "p <- ggplot(forest_data, aes(x = estimate, y = subgroup,",
    "    xmin = conf.low, xmax = conf.high)) +",
    "  geom_point() + geom_errorbarh(height = 0.2) +",
    "  geom_vline(xintercept = 1, linetype = \"dashed\") +",
    '  labs(x = "Hazard Ratio (95% CI)", y = NULL) +',
    "  theme_minimal(base_size = 11)",
    ""
  ), collapse = "\n")
}

