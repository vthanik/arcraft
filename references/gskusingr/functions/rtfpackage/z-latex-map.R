# Basic LaTeX special characters that need escaping.
# The order is important: backslash must be escaped first.
LATEX_SPECIAL_CHARS_MAP <- c(
  "\\" = "\\textbackslash{}",
  "&" = "\\&",
  "%" = "\\%",
  "$" = "\\$",
  "#" = "\\#",
  "_" = "\\_",
  "{" = "\\{",
  "}" = "\\}",
  "~" = "\\textasciitilde{}",
  "^" = "\\textasciicircum{}"
)

# Placeholders to protect our macros during the sanitization step.
# The `rtfb.*.` format is designed to be immune to all escaping steps.
# Stage 1: Convert known macros to a safe placeholder format.
LATEX_MACRO_MAP_TO_PLACEHOLDER <- c(
  "_PAGEOF_" = "rtfb.PAGEOF.",
  "_PAGE_" = "rtfb.PAGE.",
  "_PAGES_" = "rtfb.PAGES.",
  "\n" = "rtfb.NEWLINE.",
  "{\\line}" = "rtfb.NEWLINE."
)

# Stage 2: After sanitizing, convert the placeholders to the final LaTeX command.
LATEX_PLACEHOLDER_MAP_TO_LATEX <- c(
  "rtfb.PAGEOF." = "\\thepage{} of \\pageref{LastPage}",
  "rtfb.PAGE." = "\\thepage{}",
  "rtfb.PAGES." = "\\pageref{LastPage}",
  "rtfb.NEWLINE." = "\\newline "
)


